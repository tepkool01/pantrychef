import os
import json

path = './spoonacular_recipes'

retries = []

for root, directory, files in os.walk(path):
    for file in files:
        with open(path + "/" + file, encoding="utf8") as f:
            recipe = json.load(f)
        if 'status' in recipe and recipe['status'] == 'failure':
            file_split = file.split('_')
            r_id_split = file_split[2]
            r_id = r_id_split.split('.')
            if recipe['code'] == 402:  # Ran out of 'points', definitely retry this one
                retries.append(r_id[0])
            elif recipe['code'] == 400:  # Failed to parse recipe, skip
                continue
            else:
                print("NEW ISSUE FOUND!" + file)
        f.close()

print(retries)
print(len(retries))

